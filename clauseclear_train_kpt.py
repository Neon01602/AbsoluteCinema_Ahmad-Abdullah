def extract_text_from_pdf(pdf_path):
    with open(pdf_path, 'rb') as f:
        reader = PyPDF2.PdfReader(f)
        return ' '.join(page.extract_text() for page in reader.pages)

def split_into_clauses(text):
    """Split agreement text into individual clauses"""
    # Split on numbered clauses like '1.' '2.' or 'Clause 1' or by full stops + capital
    patterns = [
        r'(?=\b\d+\.\s+[A-Z])',          # "1. The Lessee..."
        r'(?=Clause\s+\d+)',               # "Clause 1"
        r'(?=[A-Z][A-Z\s]{3,}:)',          # "SECURITY DEPOSIT:"
    ]

    # Try numbered split first
    clauses = re.split(r'(?=\b\d{1,2}\.\s+[A-Z])', text)
    clauses = [c.strip() for c in clauses if len(c.strip()) > 50]

    # Fallback: split on long sentences
    if len(clauses) < 3:
        sentences = re.split(r'(?<=[.!?])\s+(?=[A-Z])', text)
        clauses = [s.strip() for s in sentences if len(s.strip()) > 80]

    return clauses[:20]  # Cap at 20 clauses for performance

