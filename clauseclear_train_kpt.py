import json
from datasets import Dataset

SYSTEM_PROMPT = """You are ClauseClear, an expert in Indian rental law and tenant rights.
When given a legal clause from an Indian rental agreement, you must respond in this exact JSON format:
{
  "plain": "<one clear sentence any renter can understand>",
  "risk": "<HIGH|MEDIUM|LOW>",
  "risk_reason": "<brief reason for risk level>",
  "counter": "<a fairer counter-clause the tenant can propose>"
}
Always be accurate about Indian rental law (Transfer of Property Act, 1882; Model Tenancy Act, 2021)."""

def format_example(example):
    output = json.dumps({
        "plain": example["plain"],
        "risk": example["risk"],
        "risk_reason": f"This clause is {example['risk'].lower()} risk because it affects tenant rights significantly.",
        "counter": example["counter"]
    }, ensure_ascii=False)

    # Gemma 3 instruction-tuned format
    text = f"<start_of_turn>user\n{SYSTEM_PROMPT}\n\nAnalyse this clause:\n{example['clause']}<end_of_turn>\n<start_of_turn>model\n{output}<end_of_turn>"
    return {"text": text}

